
	public class Animal {
		
		private String name;
		private int age;
		private boolean live;
		
		public Animal(){}
		
		public Animal(String name, int age, boolean live) {
			this.name = name;
			this.age = age;
			this.live = live;
		}
		
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public boolean getLive() {
			return live;
		}
		
		
		
	}
